# This program has a simple conversation with the user

# Asking for the user's name
name = input("What's your name? ")

# Greeting the user
print("Hello, " + name + "! Nice to meet you!")

# Asking how the user is doing
feeling = input("How are you doing today? ")

# Responding to the user's feeling
print("I'm glad to hear you're " + feeling + "!")

# Saying goodbye
print("It was nice talking to you, " + name + ". Have a great day!")
